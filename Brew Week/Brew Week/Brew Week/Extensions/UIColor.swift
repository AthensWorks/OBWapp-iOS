import UIKit

extension UIColor {
	class func brewWeekRed() -> UIColor {
		return UIColor(red:0.831, green:0.075, blue:0.157, alpha:1.000)
	}

	class func brewWeekGold() -> UIColor {
		return UIColor(red:0.733, green:0.463, blue:0.051, alpha:1.000)
	}
}
